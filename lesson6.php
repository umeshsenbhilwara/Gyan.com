<script>
    // Check translation answers
    function checkTranslationAnswers() {
        const inputs = document.querySelectorAll('#translation-exercise input');
        inputs.forEach(input => {
            const userAnswer = input.value.trim().toLowerCase();
            const correctAnswer = input.dataset.answer.toLowerCase();
            const feedback = input.nextElementSibling;
            
            if (userAnswer === correctAnswer) {
                input.classList.add('correct');
                input.classList.remove('incorrect');
                feedback.classList.add('hidden');
            } else {
                input.classList.add('incorrect');
                input.classList.remove('correct');
                feedback.classList.remove('hidden');
            }
        });
    }

    // Check fill-in-the-blanks answers
    function checkFillBlanksAnswers() {
        const inputs = document.querySelectorAll('#fill-blanks-exercise input');
        inputs.forEach(input => {
            const userAnswer = input.value.trim().toLowerCase();
            const correctAnswer = input.dataset.answer.toLowerCase();
            const feedback = input.nextElementSibling;
            
            if (userAnswer === correctAnswer) {
                input.classList.add('correct');
                input.classList.remove('incorrect');
                feedback.classList.add('hidden');
            } else {
                input.classList.add('incorrect');
                input.classList.remove('correct');
                feedback.classList.remove('hidden');
            }
        });
    }

    // Reset translation exercise
    function resetTranslationExercise() {
        const inputs = document.querySelectorAll('#translation-exercise input');
        inputs.forEach(input => {
            input.value = '';
            input.classList.remove('correct', 'incorrect');
            input.nextElementSibling.classList.add('hidden');
        });
    }

    // Reset fill-in-the-blanks exercise
    function resetFillBlanksExercise() {
        const inputs = document.querySelectorAll('#fill-blanks-exercise input');
        inputs.forEach(input => {
            input.value = '';
            input.classList.remove('correct', 'incorrect');
            input.nextElementSibling.classList.add('hidden');
        });
    }

    // Show correct answers when clicking on the question
    document.querySelectorAll('#translation-exercise p, #fill-blanks-exercise p').forEach(question => {
        if (question.nodeName === 'P' && !question.classList.contains('hidden')) {
            question.addEventListener('click', function() {
                const feedback = this.nextElementSibling.nextElementSibling;
                feedback.classList.toggle('hidden');
            });
        }
    });
</script>
