    <center><h3>Possession का यूज English में बहुत ही महत्वपूर्ण माना जाता हैं, जब भी कोई व्यक्ति किसी चीज़ या भावना (abstraction) को रखता हैं तो उसे possession कहा जाता हैं I </h3></center><br>
    
    <ul>
      <li>
        <h4>Has/Have/Had & Will have are used 3 times in English language.</h4>
        <ol>
          <li><b>I have some work (Possession)</b></li>
          <li><b>I have done work (Present Perfect Tense)</b></li>
          <li><b>I have to do work (Modal)</b></li>
          <li><b>He has to do work (Modal)</b></li>
        </ol>
      </li>
    </ul>
    
    <h4>Verb Forms:</h4>
